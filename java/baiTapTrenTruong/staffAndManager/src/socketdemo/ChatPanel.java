    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // kiểm tra xem có đoạn tin nhắn gì không
        if (getTxtMessage().getText().trim().length()==0) return;
        try { // nếu có thì gửi đoạn tin nhắn 
            getOs().writeBytes(getTxtMessage().getText()); // chuyển chuỗi thành dãy byte để chuyển đi
            getOs().write(13); // ký tự xuống hàng
            getOs().write(10); // thụt zo đầu dòng
            getOs().flush(); // đẩy dãy dữ liệu đi
            this.getTxtMessages().append("\n" + getSender() + ": " + getTxtMessage().getText()); // show lên khung chat
            getTxtMessage().setText(""); // xóa dữ liệu khung tin nhắn
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSendActionPerformed


