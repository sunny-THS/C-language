    public ChatPanel(Socket socket, String sender, String receiver) {
        initComponents();
        try {
            this.socket = socket;
            this.sender = sender;
            this.receiver = receiver;
            
            bf = new BufferedReader(new InputStreamReader(socket.getInputStream())); // nhận dữ liệu
            os = new DataOutputStream(socket.getOutputStream()); // chuyển dữ liệu
            t = new OutputThread(socket, getTxtMessages(), sender, receiver);
            t.start();
        } catch (Exception e) {
        }
        
    }
    
