    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        int row = jTable1.getSelectedRow();
        Sach s = new Sach();
        s.setMa(Integer.parseInt(dtm.getValueAt(row, 0).toString()));
        int ck = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (ck==JOptionPane.YES_OPTION)
            if (SachDAO.delSach(s)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                loadData();
            }
            else JOptionPane.showMessageDialog(this, "Xóa thất bại. Vui lòng thử lại", "Thông Báo", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            int count = SachDAO.countSach();
            int n = 0;
            Sach s = new Sach();
            if (SachDAO.bCheckTen(tfTen.getText())) {
                int ck = JOptionPane.showConfirmDialog(this, "Tên sách đã tồn tại bạn có muốn cập nhật hay không?", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (ck == JOptionPane.YES_OPTION) {
                    s.setTieuDe(tfTen.getText());
                    s.setMoTa(tfMoTa.getText());
                    s.setNamXB(Integer.parseInt(tfNamxb.getText()));
                    s.setTacGia(tfTacGia.getText());
                    s.setTheLoai(tfType.getText());
                    
                    int row = jTable1.getSelectedRow();
                    s.setMa((int) dtm.getValueAt(row, 0));
                    
                    n = SachDAO.udSach(s)?1:0;
                }
            }else {
                s.setTieuDe(tfTen.getText());
                s.setMoTa(tfMoTa.getText());
                s.setNamXB(Integer.parseInt(tfNamxb.getText()));
                s.setTacGia(tfTacGia.getText());
                s.setTheLoai(tfType.getText());
                s.setMa(count+1);
                n = SachDAO.insertSach(s)?1:0;
            }
            if (n>0) {
                loadData();
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin sách thành công", "thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            else JOptionPane.showMessageDialog(this, "Cập nhật thông tin sách Thất bại vui lòng thử lại", "thông báo", JOptionPane.WARNING_MESSAGE);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi. Vui lòng thử lại\n"+e.getMessage(), "thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

