    private void loadData() {
        jComboBox2.removeAllItems();
        tfType.setVisible(true);
        jComboBox2.setVisible(false);
        dtm.setRowCount(0);
        ArrayList<Sach> arS = SachDAO.showLst();
        arS.forEach(sach -> {
            Vector<Object> v = new Vector<Object>();
            v.add(sach.getMa());
            v.add(sach.getTieuDe());
            v.add(sach.getTacGia());
            v.add(sach.getNamXB());
            v.add(sach.getTheLoai());
            v.add(sach.getMoTa());
            dtm.addRow(v);
        });
    }
